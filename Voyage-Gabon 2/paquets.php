            <p>
              Recouvert en grande partie par la forêt
              équatoriale, le Gabon s'est naturellement
              imposé comme un écosystème favorable à
              l'installation d'espèces animales. La
              végétation luxuriante et les marécages
              constituent notamment un excellent habitant
              pour de nombreuses espèces de serpents dont le
              célèbre mamba noir, réputé pour sa dangerosité
              (bien qu'il évite généralement tout contact
              avec l'être humain). Le Gabon est aussi une
              terre d'accueil pour les gorilles, en voie
              d'extinction. Dans la canopée, il est aisé
              d'observer le chatoiement des couleurs
              d'oiseaux tropicaux. Le long des cours d'eau
              se prélassent crocodiles et autres grands
              mammifères africains.
