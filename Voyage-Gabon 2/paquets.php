            <h3>Les arts et la culture</h3>
            <p>
              Les arts ne sont pas très développés au Gabon
              car l'artisan-artiste a d'abord et surtout une
              fonction utilitaire et davantage sociale et
              spirituelle que purement artistique. Ainsi on
              trouve au Gabon une quantité de masques
              servant aux rituels et aux différents
              événements religieux. Le voyageur trouvera peu
              d'objets d'art-artisanat sur les marchés pour
              la simple raison que leur création n'est pas
              destinée à la vente mais à l'initiation
              spirituelle et sociale. Les danses
              traditionnelles sont innombrables, en fonction
              des ethnies, des coutumes, des saisons? Un
              spectacle passionnant par son aspect
              sociologique autant qu'artistique. Mais de
              tous les genres artistiques c'est
              véritablement la musique qui est le plus
