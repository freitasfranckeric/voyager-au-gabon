            <h3>Les paysages</h3>
            <p>
              Le relief du Gabon est composé de plaines
              côtières étroites, derrière lesquelles
              s'étendent des collines à l'intérieur, des
              savanes à l'est et au sud. Le pays paraît plat
              au premier abord mais une fois le littoral
              dépassé, on peut distinguer trois zones
              montagneuses. Au nord s'érigent les monts de
              Cristal qui culminent à 900m d'altitude et qui
              se poursuivent au-delà de la Guinée
              équatoriale. Au sud se dressent le plus
              important massif du Gabon, le massif Du
              Chaillu que domine le mont Ibopundji, point
              culminant du pays à 1575m d'altitude, et enfin
              le massif du Mayombé, avec le mont Pelé avec
              ses 795m d'altitude.
            </p>
